package main

import (
	"log"
	"os"
)

// loadIconFromFile loads the icon from printerIcon.ico file
func loadIconFromFile(filename string) []byte {
	data, err := os.ReadFile(filename)
	if err != nil {
		log.Printf("Error al cargar el icono desde %s: %v", filename, err)
		// Return default icon if file not found
		return getDefaultIcon()
	}
	return data
}

// loadGreenIcon loads the green icon for connected state
func loadGreenIcon() []byte {
	return loadIconFromFile("printerGreen.ico")
}

// loadRedIcon loads the red icon for disconnected state
func loadRedIcon() []byte {
	return loadIconFromFile("printerRed.ico")
}

